package main

import (
	"fmt"
	"regexp"
	"slices"
	"strings"

	"github.com/mongodb/terraform-provider-mongodbatlas/tools/codegen/codespec"
)

const tfplugindocsMarker = "<!-- schema generated by tfplugindocs -->"

var (
	attrLineRegex         = regexp.MustCompile("^- `([^`]+)` \\(([^)]+)\\) (.+)$")
	topSectionRegex       = regexp.MustCompile(`^### (Required|Optional|Read-Only)$`)
	nestedSubSectionRegex = regexp.MustCompile(`^(Required|Optional|Read-Only):$`)
	nestedSchemaRegex     = regexp.MustCompile("^### Nested Schema for `([^`]+)`$")
)

type parsedAttribute struct {
	name        string
	typeInfo    string
	description string
	rawLine     string
}

type typeEntry struct {
	attr       parsedAttribute
	isRequired bool
}

type replacement struct {
	newContent []string
	startLine  int
	endLine    int
}

// RestructurePolymorphicDocs processes a full markdown document, restructuring
// sections that have polymorphic attributes based on the discriminator model.
// When isResource is false (data sources), attributes are never marked as required.
func RestructurePolymorphicDocs(content string, discriminators map[string]*codespec.Discriminator, isResource bool) string {
	if !strings.Contains(content, tfplugindocsMarker) {
		return content
	}
	if len(discriminators) == 0 {
		return content
	}

	lines := strings.Split(content, "\n")
	markerIdx := findMarkerIndex(lines)
	if markerIdx == -1 {
		return content
	}

	schemaEnd := findSchemaEnd(lines, markerIdx)
	replacements := findReplacements(lines, markerIdx, schemaEnd, discriminators, isResource)

	if len(replacements) == 0 {
		return content
	}

	return applyReplacements(lines, replacements)
}

func findMarkerIndex(lines []string) int {
	for i, line := range lines {
		if strings.TrimSpace(line) == tfplugindocsMarker {
			return i
		}
	}
	return -1
}

// findSchemaEnd returns the line index of the first level-2 heading after "## Schema"
// (typically "## Import" for resources). For data sources the schema extends to EOF.
// Uses a generic heading check rather than hardcoding "## Import" so unexpected headings
// act as a defensive boundary instead of being silently scanned past.
func findSchemaEnd(lines []string, markerIdx int) int {
	for i := markerIdx + 1; i < len(lines); i++ {
		line := lines[i]
		if strings.HasPrefix(line, "## ") && line != "## Schema" {
			return i
		}
	}
	return len(lines)
}

func findReplacements(lines []string, start, end int, discriminators map[string]*codespec.Discriminator, isResource bool) []replacement {
	var replacements []replacement

	activeDiscriminator := discriminators[""]

	i := start
	for i < end {
		line := lines[i]

		if m := nestedSchemaRegex.FindStringSubmatch(line); m != nil {
			nestedName := extractNestedName(m[1])
			if d, ok := discriminators[nestedName]; ok {
				activeDiscriminator = d
			} else {
				activeDiscriminator = nil
			}
			i++
			continue
		}

		if topSectionRegex.MatchString(line) || nestedSubSectionRegex.MatchString(line) {
			if activeDiscriminator != nil {
				attrStart, attrEnd, attrs := collectAttributeBlock(lines, i+1, end)
				if len(attrs) > 0 {
					common, typeGroups := classifyAttributes(attrs, activeDiscriminator, isResource)
					if len(typeGroups) > 0 {
						replacements = append(replacements, replacement{
							startLine:  attrStart,
							endLine:    attrEnd,
							newContent: buildRestructuredBlock(common, typeGroups, activeDiscriminator.PropertyName.TFSchemaName),
						})
					}
				}
				i = attrEnd
				continue
			}
		}
		i++
	}

	return replacements
}

// collectAttributeBlock finds the contiguous block of attribute lines (with
// interleaved blank lines) after a section heading.
func collectAttributeBlock(lines []string, afterHeading, schemaEnd int) (attrStart, attrEnd int, attrs []parsedAttribute) {
	i := afterHeading
	for i < schemaEnd && strings.TrimSpace(lines[i]) == "" {
		i++
	}
	attrStart = i

	lastAttrIdx := i
	for i < schemaEnd {
		line := lines[i]
		if m := attrLineRegex.FindStringSubmatch(line); m != nil {
			attrs = append(attrs, parsedAttribute{
				name:        m[1],
				typeInfo:    m[2],
				description: m[3],
				rawLine:     m[0],
			})
			lastAttrIdx = i + 1
			i++
			continue
		}
		if strings.TrimSpace(line) == "" {
			i++
			continue
		}
		break
	}
	attrEnd = lastAttrIdx
	return
}

// classifyAttributes uses the discriminator model to split attributes into
// common (not type-specific) and type-grouped (polymorphic) buckets.
// When isResource is false, no attributes are flagged as required.
func classifyAttributes(attrs []parsedAttribute, disc *codespec.Discriminator, isResource bool) (common []parsedAttribute, typeGroups map[string][]typeEntry) {
	typeGroups = make(map[string][]typeEntry)

	reverseIndex := buildReverseIndex(disc)

	for _, attr := range attrs {
		if attr.name == disc.PropertyName.TFSchemaName {
			common = append(common, attr)
			continue
		}

		info, found := reverseIndex[attr.name]
		if !found {
			common = append(common, attr)
			continue
		}

		allTypes := append(slices.Clone(info.requiredTypes), info.optionalTypes...)
		for _, typeName := range allTypes {
			isReq := isResource && slices.Contains(info.requiredTypes, typeName)
			typeGroups[typeName] = append(typeGroups[typeName], typeEntry{
				attr:       attr,
				isRequired: isReq,
			})
		}
	}
	return common, typeGroups
}

type attrTypeInfo struct {
	requiredTypes []string
	optionalTypes []string
}

func buildReverseIndex(disc *codespec.Discriminator) map[string]*attrTypeInfo {
	index := make(map[string]*attrTypeInfo)

	for typeName, variant := range disc.Mapping {
		requiredSet := make(map[string]bool, len(variant.Required))
		for _, r := range variant.Required {
			requiredSet[r.TFSchemaName] = true
		}

		for _, a := range variant.Allowed {
			info, ok := index[a.TFSchemaName]
			if !ok {
				info = &attrTypeInfo{}
				index[a.TFSchemaName] = info
			}
			if requiredSet[a.TFSchemaName] {
				info.requiredTypes = append(info.requiredTypes, typeName)
			} else {
				info.optionalTypes = append(info.optionalTypes, typeName)
			}
		}
	}

	for _, info := range index {
		slices.Sort(info.requiredTypes)
		slices.Sort(info.optionalTypes)
	}

	return index
}

func buildRestructuredBlock(common []parsedAttribute, typeGroups map[string][]typeEntry, discriminatorName string) []string {
	var result []string
	for _, attr := range common {
		result = append(result, attr.rawLine)
	}

	if len(common) > 0 {
		result = append(result, "")
	}
	result = append(result, fmt.Sprintf("The following attributes depend on the value of `%s`:", discriminatorName))

	for _, typeName := range sortedTypeKeys(typeGroups) {
		result = append(result, "", fmt.Sprintf("#### `%s`", typeName), "")

		entries := typeGroups[typeName]
		slices.SortFunc(entries, func(a, b typeEntry) int {
			return strings.Compare(a.attr.name, b.attr.name)
		})
		for j := range entries {
			result = append(result, renderTypeAttribute(&entries[j]))
		}
	}

	return result
}

func sortedTypeKeys(m map[string][]typeEntry) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	slices.Sort(keys)
	return keys
}

func renderTypeAttribute(entry *typeEntry) string {
	desc := entry.attr.description
	if entry.isRequired {
		desc = "**Required.** " + desc
	}
	return fmt.Sprintf("- `%s` (%s) %s", entry.attr.name, entry.attr.typeInfo, desc)
}

func applyReplacements(lines []string, replacements []replacement) string {
	for i := len(replacements) - 1; i >= 0; i-- {
		r := replacements[i]
		var newLines []string
		newLines = append(newLines, lines[:r.startLine]...)
		newLines = append(newLines, r.newContent...)
		newLines = append(newLines, lines[r.endLine:]...)
		lines = newLines
	}
	return strings.Join(lines, "\n")
}

// extractNestedName gets the leaf name from a nested schema path like "results.settings" -> "settings",
// or "results" -> "results".
// Limitation: if two nested schemas at different paths share a leaf name (e.g. "a.config" and "b.config"),
// they would collide in the discriminators map.
func extractNestedName(path string) string {
	parts := strings.Split(path, ".")
	return parts[len(parts)-1]
}
